                    </p>
                    <p><b>Desire for sanctity </b></p>
                    <p>
                        The children of the Nicolo family often went their paternal grand-uncle and aunt who had holiday villa in St. Michael’s . This couple, having no children of their own, entertained on their home. Brigida was their favorite. The children affectionately called them grand-father and grand –mother. The good old man was very pious and devoted and enjoyed reading the lives of saints which Brigida listened to eagerly. Even at that tender age she understood that to become a saint was a beautiful thing and this aroused in her a desire for sanctity beyond her age, as though the Lord was anticipating His action in her with special enlightenment and often happens with those who are destined to be models of sanctity.

                        Her great desire for sanctity right from her childhood, the vision she had at the age of four of the beauty of heaven and the transitory of this world, her love for the poor, zeal for souls, eagerness to practice every virtue, the great desire to become a nun and a saint etc. singled her out as an extra-ordinary child.

                    </p>





